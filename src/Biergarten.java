
public class Biergarten {

	private int node;
	private int coordX;
	private int coordY;

	public Biergarten(int node, int coordX, int coordY) {
		super();
		this.node = node;
		this.coordX = coordX;
		this.coordY = coordY;
	}

	public int getNode() {
		return node;
	}

	public int getCoordX() {
		return coordX;
	}

	public void setCoordX(int coordX) {
		this.coordX = coordX;
	}

	public int getCoordY() {
		return coordY;
	}

	public void setCoordY(int coordY) {
		this.coordY = coordY;
	}



}
